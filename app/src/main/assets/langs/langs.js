function getTranslationByLang(langCode, key) {
    const index = langMap[langCode];
    const translationArray = translations[key];

    if (translationArray && index !== undefined && index < translationArray.length) {
        const translation = translationArray[index];
        if (translation && translation.trim() !== "") {
            return translation;
        }
    }

    return translationArray ? translationArray[0] : '';
}

function applyTranslations(langCode) {
    const elementsToTranslate = document.querySelectorAll('[data-translate]');
    elementsToTranslate.forEach(el => {
        const translationKey = el.getAttribute('data-translate');
        el.textContent = getTranslationByLang(langCode, translationKey);
    });
}
